    public void CargarComboNiveles() {
        GestorBD gestor = new GestorBD();

        ArrayList<Nivel> lista = gestor.ObtenerNiveles();

        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (Nivel t : lista) {
            model.addElement(t);
        }
        cboNivel.setModel(model);
    }

