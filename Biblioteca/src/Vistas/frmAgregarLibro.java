    private void btnElejirAutorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElejirAutorActionPerformed
        //paso el valor de la fila seleccionada a los txts 
        try {
            txtNombre.setText(tblAutores.getValueAt(tblAutores.getSelectedRow(), 1).toString());
            txtDocumento.setText(tblAutores.getValueAt(tblAutores.getSelectedRow(), 0).toString());

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "seleccione un autor para registrar un libro");
        }
    }//GEN-LAST:event_btnElejirAutorActionPerformed

    private void btnAgregarLibroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarLibroActionPerformed
        try {
            //paso por variables el valor de cata textbox
            Autor a = new Autor();
            
            int dni = Integer.parseInt(txtDocumento.getText());
            
            a.setDni(dni);
            
            int Doc = Integer.parseInt(txtCodigo.getText());
            String Desc = txtDescripcion.getText();
            Prestamo nac = (Prestamo) cboPrestamo.getSelectedItem();

            //instancias un nuevo producto
            //el cero lo colocas por que es un campo identity 
            //y asi no generas algun error
            Libro l = new Libro(Doc, Desc, nac, a);

            //instancias el gestor y llamas el metodo para realizar el insert  
            GestorBD gestor = new GestorBD();

            boolean consulta = gestor.AgregarLibro(l);
            if (consulta) {
                JOptionPane.showMessageDialog(this, "La venta se registro exitosamente");
            } else {
                JOptionPane.showMessageDialog(this, "La venta no se registro exitosamente");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "debe ingresar datos validos");
        }
        CargarListaAutores();
    }//GEN-LAST:event_btnAgregarLibroActionPerformed

