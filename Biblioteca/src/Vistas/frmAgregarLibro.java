    public void CargarCombo() {
        GestorBD gestor = new GestorBD();

        ArrayList<Prestamo> lista = gestor.ObtenerPrestamos();

        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (Prestamo n : lista) {
            model.addElement(n);
        }
        cboPrestamo.setModel(model);
    }

    private void CargarListaAutores() {
        GestorBD gestor = new GestorBD();
        ArrayList<Autor> venta = gestor.ObtenerAutores();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Dni", "Nombre", "Nacionalidad"});
        for (Autor v : venta) {
            modelo.addRow(new Object[]{v.getDni(), v.getNombre(), v.getNacionalidad()});
        }
        tblAutores.setModel(modelo);
    }
    private void CargarListaLibros()
    {
        
        GestorBD gestor = new GestorBD();
        ArrayList<Libro> Lista = gestor.ObtenerLibros();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Codigo","Descripcion","Nombre","Dni","Nacionalidad"});
        for (Libro v : Lista) {
            modelo.addRow(new Object[]{v.getCodigo(),v.getDescripcion(),v.getAutor().getNombre(),v.getAutor().getDni(),v.getAutor().getNacionalidad().getNacionalidad()});
        }
        tblLibros.setModel(modelo);
    }

