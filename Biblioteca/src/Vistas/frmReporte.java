    private void btnNacionalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNacionalesActionPerformed

        GestorBD gestor = new GestorBD();
        ArrayList<Libro> venta = gestor.ObtenerLibrosNacionales();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Nombre", "Nacionalidad", "codigo", "libro"});
        for (Libro v : venta) {
            modelo.addRow(new Object[]{v.getAutor().getNombre(), v.getAutor().getNacionalidad(), v.getCodigo(), v.getDescripcion()});
        }
        tblReporte.setModel(modelo);

    }//GEN-LAST:event_btnNacionalesActionPerformed

    private void btnCantidadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCantidadActionPerformed
        GestorBD gestor = new GestorBD();
        ArrayList<Reporte> venta = gestor.cantidaddelibros();
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.setColumnIdentifiers(new String[]{"Cantidad de libros Registrados"});
        for (Reporte v : venta) {
            modelo.addRow(new Object[]{v.getTotaldelibros()});
        }
        tblReporte.setModel(modelo);
    }//GEN-LAST:event_btnCantidadActionPerformed

