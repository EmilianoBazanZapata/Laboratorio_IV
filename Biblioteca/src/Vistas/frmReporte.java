    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        try {
            int doc = Integer.parseInt(txtDocumento.getText());

            System.out.println(doc);
            GestorBD gestor = new GestorBD();
            ArrayList<Libro> venta = gestor.ObtenerAutores(doc);
            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new String[]{"Nombre", "DNI", "Nacionalidad", "codigo", "libro"});
            for (Libro v : venta) {
                modelo.addRow(new Object[]{v.getAutor().getNombre(), v.getAutor().getDni(),
                    v.getAutor().getNacionalidad().getNacionalidad(),
                    v.getCodigo(), v.getDescripcion()});
            }
            tblReporte.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ingrese el documento de un autor");
        }


    }//GEN-LAST:event_btnConsultarActionPerformed

